                                        Matcher tamanoMatcher = tamanoRegex.matcher(archivoInfo);
                                        if (tamanoMatcher.find()) {
                                            String tamanoStr = tamanoMatcher.group(2);
                                            tamanoArchivo = Integer.parseInt(tamanoStr);
                                        }

